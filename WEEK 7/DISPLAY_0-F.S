SWI 0X200    ;SET 8 SEGMENT DISPLAY TO LIGHT UP

BL DELAY
SUB R1,R1,#1
SUB R5,R5,#1
CMP R5,#0
BNE BACK2
B AGAIN

DELAY: 
MOV R4,#64000
LOOP3:
SUB R4,R4,#1
CMP R4,#0
BGE LOOP3
