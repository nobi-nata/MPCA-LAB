BACK1:
LDRB R0,[R1]
SWI 0X200  ;SET 8 SEGMENT DISPLAY TO LIGHT UP

BL DELAY

ADD R1,R1,#1
SUB R5,R5,#1
CMP R5,#0
BNE BACK1

B AGAIN

LOOP2:
MOV R5,#16
LDR R1,=F

BACK2:
